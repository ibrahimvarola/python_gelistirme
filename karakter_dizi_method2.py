#upper() ve lower() metodları
dizi1 = "IBRAHIM"
dizi2 = "varola"

print(dizi1.lower())
print(dizi2.upper())

#isupper() ve islower() metodları bize karakter dizisinin büyük veya küçük karakterlerden oluştuğunu belirtir.

